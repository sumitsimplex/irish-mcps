// ─── Tool Dispatch ────────────────────────────────────────────────────────────

async function callTool(
  name: string,
  args: Record<string, unknown>,
  env: Env,
): Promise<string> {
  const route = args.route_number ? String(args.route_number) : null;
  const op = args.operator && args.operator !== "all" ? String(args.operator) : null;
  const limit = args.limit ? Math.min(Number(args.limit), 100) : 30;

  switch (name) {
    case "query":
      return naturalLanguageQuery(String(args.query ?? ""), env.NTA_API_KEY);
    case "get_vehicle_positions":
      return getVehiclePositions(env.NTA_API_KEY, route, op, limit);
    case "get_trip_updates":
      return getTripUpdates(env.NTA_API_KEY, route, op, limit);
    case "get_alerts":
      return getAlerts(env.NTA_API_KEY, route, op);
    default:
      throw new Error(`Unknown tool: ${name}`);
  }
}

// ─── MCP JSON-RPC Handler ─────────────────────────────────────────────────────

const CORS = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Methods": "POST, GET, OPTIONS",
  "Access-Control-Allow-Headers": "Content-Type, Accept",
};

function json(body: unknown, status = 200): Response {
  return new Response(JSON.stringify(body), {
    status,
    headers: { ...CORS, "Content-Type": "application/json" },
  });
}

async function handleMCP(req: Request, env: Env): Promise<Response> {
  if (req.method === "OPTIONS") return new Response(null, { headers: CORS });

  if (req.method === "GET") {
    return json({
      name: "Dublin Bus & Bus Éireann Realtime MCP",
      version: "1.0.0",
      description: "Real-time Dublin Bus & Bus Éireann data via NTA GTFS-R",
      tools: TOOLS.map(t => t.name),
    });
  }

  if (req.method !== "POST")
    return new Response("Method Not Allowed", { status: 405, headers: CORS });

  let body: { jsonrpc?: string; method?: string; params?: unknown; id?: unknown };
  try {
    body = await req.json();
  } catch {
    return json(
      { jsonrpc: "2.0", error: { code: -32700, message: "Parse error" }, id: null },
      400,
    );
  }

  const id = body.id ?? null;
  const ok = (result: unknown) => json({ jsonrpc: "2.0", result, id });
  const err = (code: number, msg: string) =>
    json({ jsonrpc: "2.0", error: { code, message: msg }, id });

  switch (body.method) {
    case "initialize":
      return ok({
        protocolVersion: "2024-11-05",
        serverInfo: { name: "Dublin Bus & Bus Éireann Realtime MCP", version: "1.0.0" },
        capabilities: { tools: {} },
      });

    case "notifications/initialized":
      return new Response(null, { status: 204, headers: CORS });

    case "ping":
      return ok({});

    case "tools/list":
      return ok({ tools: TOOLS });

    case "tools/call": {
      const p = body.params as
        | { name?: string; arguments?: Record<string, unknown> }
        | undefined;
      if (!p?.name) return err(-32602, "Missing tool name");
      try {
        const text = await callTool(p.name, p.arguments ?? {}, env);
        return ok({ content: [{ type: "text", text }] });
      } catch (e) {
        return err(-32000, e instanceof Error ? e.message : "Tool failed");
      }
    }

    default:
      return err(-32601, `Method not found: ${body.method}`);
  }
}

// ─── Worker Entry Point ───────────────────────────────────────────────────────

export default {
  async fetch(request: Request, env: Env): Promise<Response> {
    const { pathname } = new URL(request.url);

    if (pathname === "/mcp" || pathname === "/mcp/") return handleMCP(request, env);

    if (pathname === "/health") {
      return json({ status: "ok", service: "Dublin Bus & Bus Éireann Realtime MCP", version: "1.0.0" });
    }

    if (pathname === "/" || pathname === "") {
      return json({
        service: "Dublin Bus & Bus Éireann Realtime MCP",
        mcp_endpoint: "/mcp",
        tools: TOOLS.map(t => ({ name: t.name, description: t.description })),
      });
    }

    return new Response("Not Found", { status: 404, headers: CORS });
  },
};
